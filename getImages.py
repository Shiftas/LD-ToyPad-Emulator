def get_image_url(character_name, api_key, search_engine_id):
    search_url = "https://www.googleapis.com/customsearch/v1"
    params = {
        'q': character_name,
        'cx': search_engine_id,
        'key': api_key,
        'searchType': 'image',
        'num': 1
    }
    response = requests.get(search_url, params=params)
    results = response.json()
    # print(f"{results}")

    if 'items' in results:
        return results['items'][0]['link']
    return None


